def get_statistics(start_date=None, end_date=None, offer_id=None, pp_name=None):
    try:
        conn = sqlite3.connect('conversions.db')
        cursor = conn.cursor()

        query = '''
            SELECT pp_name, offer_id, SUM(revenue), COUNT(*)
            FROM conversions
            WHERE 1=1
        '''
        params = []

        if start_date:
            query += ' AND conversion_date >= ?'
            params.append(start_date)
        if end_date:
            query += ' AND conversion_date <= ?'
            params.append(end_date)
        if offer_id:
            query += ' AND offer_id = ?'
            params.append(offer_id)
        if pp_name:
            query += ' AND pp_name = ?'
            params.append(pp_name)

        query += ' GROUP BY pp_name, offer_id'
        cursor.execute(query, params)
        results = cursor.fetchall()
        conn.close()
        return results
    except sqlite3.Error as e:
        logger.error(f"ĞÑˆĞ¸Ğ±ĞºĞ° Ğ¿Ñ€Ğ¸ Ñ€Ğ°Ğ±Ğ¾Ñ‚Ğµ Ñ Ğ±Ğ°Ğ·Ğ¾Ğ¹ Ğ´Ğ°Ğ½Ğ½Ñ‹Ñ…: {e}")
